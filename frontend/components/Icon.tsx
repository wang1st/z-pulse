export default function Icon({ name, className = '', style, size = 16 }: IconProps) {
  const sizeValue = typeof size === 'number' ? `${size}px` : size
  const sizeStyle: React.CSSProperties = { 
    width: sizeValue, 
    height: sizeValue,
    display: 'inline-block',
    verticalAlign: 'middle',
    flexShrink: 0,
    ...style 
  }
  
  // 优先使用 SVG 图标（更可靠）
  if (svgIcons[name]) {
    return (
      <span 
        className={`inline-flex items-center justify-center ${className}`}
        style={sizeStyle}
        aria-hidden="true"
      >
        {React.cloneElement(svgIcons[name] as React.ReactElement, {
          width: sizeValue,
          height: sizeValue,
          style: {
            width: '100%',
            height: '100%',
            display: 'block',
          }
        })}
      </span>
    )
  }
  
  // 备用：使用 iconfont
  const iconClass = iconMap[name] || name
  return (
    <i 
      className={`iconfont ${iconClass} ${className}`}
      style={{ fontSize: sizeValue, ...style }}
      aria-hidden="true"
    />
  )
}

